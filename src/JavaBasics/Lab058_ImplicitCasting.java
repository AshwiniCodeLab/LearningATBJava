package JavaBasics;

//In this example, we are adding an integer and a double number, storing the result in a double variable to display the sum, implicitly casting the integer to a double during the addition:
public class Lab058_ImplicitCasting {
    public static void main(String[] args) {
        int i=10;
        double j=90.8;
        double sum= i+j;
        System.out.println("The sum of "+i+" and "+j+" is: "+sum);
    }
}
